from easygraphics.turtle import *

create_world()

set_speed(50)
while is_run():
    fd(100)
    rt(90)
    fd(100)
    rt(90)
    fd(50)
    rt(90)
    fd(50)
    rt(90)
    fd(100)
    rt(90)
    fd(25)
    rt(90)
    fd(25)
    rt(90)
    fd(50)

    rt(10)
    forward(50)

pause()
close_world()
